func GetMimetypes() map[string][]string {
	return mimetypes
}

func GetMimetype(key string) ([]string, error) {
	val, ok := mimetypes[strings.TrimLeft(key, ".")]
	var err error
	if !ok {
		err = fmt.Errorf("%s does not exist", key)
	}

	return val, err
}
